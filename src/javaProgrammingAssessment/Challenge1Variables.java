package javaProgrammingAssessment;

public class Challenge1Variables {
    public static void main(String[] args) {
        int integer_variable = 25;
        double double_variable = 3.14d;
        boolean boolean_variable = true;
        char character_variable = 'A';

        System.out.println("Integer: " + integer_variable );
        System.out.println("Double: " + double_variable);
        System.out.println("Boolean: " + boolean_variable);
        System.out.println("Character: " + character_variable);

    }
}
